public class SamAverageMarks{
	
	public static void main (String[] args){
		
		// Create int variable mathsMarks to store Sam's marks of Maths
			int  mathsMarks = 94;

		// Create int variable physicsMarks to store Sam's marks of Physics
			int  physicsMarks = 95;

		// Create int variable chemistryMarks to store Sam's marks of Chemistry
			int  chemistryMarks = 96;

		// Calculation of Average marks of PCM
                        int averageMarks = (mathsMarks + physicsMarks + chemistryMarks)/3;

		// Printing the Result
			System.out.print("Sam's average marks in PCM is "+averageMarks);  

		}

}